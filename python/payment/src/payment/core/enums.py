    EASY_PAY = "EASY_PAY"
    MOBILE_PHONE = "MOBILE_PHONE"
    TRANSFER = "TRANSFER"
    CULTURE_GIFT_CERTIFICATE = "CULTURE_GIFT_CERTIFICATE"
    BOOK_CULTURE_GIFT_CERTIFICATE = "BOOK_CULTURE_GIFT_CERTIFICATE"
    GAME_CULTURE_GIFT_CERTIFICATE = "GAME_CULTURE_GIFT_CERTIFICATE"
